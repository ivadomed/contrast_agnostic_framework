#!/usr/bin/env python
"""
Phase 2B – Native-Mask PyRadiomics Feature Extraction Engine

For every ON-Harmony scan + its corresponding native brain mask (generated by
generate_masks.py), extracts 107 radiomic features across 7 classes using the
whole-brain ROI.  Results are appended to a single CSV.

Feature classes (PyRadiomics):
  First-Order (18), 3D Shape (14), GLCM (24),
  GLSZM (16), GLRLM (16), NGTDM (5), GLDM (14)  → 107 total

Modality handling:
  • DWI   – b0 volume (b < 100); mean over all b0 volumes if multiple
  • BOLD  – vol-0 (reference volume)
  • SWI   – configurable: magnitude only (default) or all parts
  • fmap/epi – included (EPI brain volumes, treated like func)
  • defacemask – always skipped

Output:
  results/eval/v19/manifold_analysis/radiomics/on_harmony_features.csv

Usage (run inside tmux):
  tmux new -s radiomics_extract
  run_job --gpus 0 --slot 0 --wait -- .venv/bin/python analysis/contrast_manifold/scripts/extract_features_native.py \
      --bids_dir        data/ON-Harmony \
      --derivatives_dir data/ON-Harmony/derivatives \
      --n_workers       6
"""

import argparse
import logging
import warnings
from concurrent.futures import ProcessPoolExecutor, as_completed
from pathlib import Path

import nibabel as nib
import numpy as np
import pandas as pd
import SimpleITK as sitk
from radiomics import featureextractor
from tqdm import tqdm

warnings.filterwarnings("ignore")
logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
log = logging.getLogger(__name__)

# Silence radiomics / pykwalify noise
for _lg in ("radiomics", "radiomics.glcm", "pykwalify", "pykwalify.core"):
    logging.getLogger(_lg).setLevel(logging.ERROR)

# ─── Scanner / vendor table ───────────────────────────────────────────────────

SCANNER_INFO: dict[str, tuple[str, str]] = {
    "NOT1ACH": ("Philips Achieva",     "Philips"),
    "NOT2ING": ("Philips Ingenia",     "Philips"),
    "NOT3GEM": ("GE MR750",            "GE"),
    "NOT4GEP": ("GE Premier 48ch",     "GE"),
    "OXF1PRI": ("Siemens Prisma 32ch", "Siemens"),
    "OXF2PRI": ("Siemens Prisma 64ch", "Siemens"),
    "OXF3TRI": ("Siemens Trio",        "Siemens"),
    "OXF4GEP": ("GE Premier 21ch",     "GE"),
}

SKIP_SUFFIXES = {"defacemask"}

# SWI parts considered "phase-like" (not magnitude signal)
PHASE_PARTS = {"phase_GRE", "imag_GRE", "real_GRE"}

# ─── BIDS parsing ─────────────────────────────────────────────────────────────


def parse_bids(filename: str) -> dict:
    stem = filename.replace(".nii.gz", "").replace(".nii", "")
    entities: dict = {}
    for part in stem.split("_"):
        if "-" in part:
            k, v = part.split("-", 1)
            entities[k] = v
        else:
            entities["suffix"] = part
    return entities


def make_modality_id(entities: dict) -> str:
    suffix = entities.get("suffix", "unknown")
    tags = [f"{k}-{entities[k]}" for k in ("task", "acq", "dir", "run", "echo", "part")
            if k in entities]
    return f"{suffix}_{'_'.join(tags)}" if tags else suffix


def parse_scanner(ses: str) -> tuple[str, str]:
    code = ses.replace("ses-", "")[:7]
    model, vendor = SCANNER_INFO.get(code, ("Unknown", "Unknown"))
    return model, vendor


def make_acq_tag(entities: dict) -> str:
    """Compact acquisition variant string for the metadata CSV."""
    tags = [f"{k}-{entities[k]}" for k in ("acq", "dir", "run", "echo", "part", "task")
            if k in entities]
    return "_".join(tags) if tags else ""


# ─── File discovery ───────────────────────────────────────────────────────────


def discover_scans(
    bids_root: Path,
    deriv_root: Path,
    swi_mag_only: bool = True,
    swi_echoes: list[str] | None = None,
    no_mask: bool = False,
) -> list[dict]:
    """
    Return a list of dicts, one per scan to process, each carrying:
      path, mask_path, entities, modality_id, acq_tag, sub, ses

    If no_mask=True, mask_path is set to None and all source scans are included
    regardless of whether a native mask exists.
    """
    records = []
    masks_root = deriv_root / "masks"

    for nii in sorted(bids_root.rglob("*.nii.gz")):
        if "derivatives" in nii.parts:
            continue
        entities = parse_bids(nii.name)
        suffix   = entities.get("suffix", "")

        if suffix in SKIP_SUFFIXES:
            continue

        # SWI filtering
        if suffix == "GRE":
            part = entities.get("part", "")
            if swi_mag_only and not part.startswith("mag"):
                continue
            if swi_echoes and entities.get("echo") not in swi_echoes:
                continue

        if no_mask:
            mask_path = None
        else:
            rel       = nii.relative_to(bids_root)
            mask_name = nii.name.replace(".nii.gz", "_mask.nii.gz")
            mask_path = masks_root / rel.parent / mask_name
            if not mask_path.exists():
                continue   # mask not yet generated – silently skip

        records.append({
            "path":        nii,
            "mask_path":   mask_path,
            "entities":    entities,
            "suffix":      suffix,
            "modality_id": make_modality_id(entities),
            "acq_tag":     make_acq_tag(entities),
            "sub":         entities.get("sub", "?"),
            "ses":         entities.get("ses", "?"),
        })

    return records


# ─── Volume extraction ────────────────────────────────────────────────────────


def extract_3d(nii_path: Path, entities: dict) -> tuple[np.ndarray, nib.Nifti1Image]:
    """
    Return (3D float32 array, reference nibabel image for affine/header).
    Handles 4D → 3D for DWI (b0 mean) and BOLD (vol-0).
    """
    img  = nib.load(str(nii_path))
    data = img.get_fdata(dtype=np.float32)

    if data.ndim == 3:
        return data, img

    if data.ndim == 4:
        suffix = entities.get("suffix", "")
        if suffix == "dwi":
            bval_path = nii_path.with_suffix("").with_suffix(".bval")
            if bval_path.exists():
                bvals   = np.loadtxt(bval_path)
                b0_idx  = np.where(bvals < 100)[0]
                if b0_idx.size > 0:
                    vol3d = data[..., b0_idx].mean(axis=-1)
                    # Create a pseudo-3D header
                    hdr = img.header.copy()
                    hdr.set_data_shape(vol3d.shape)
                    ref3d = nib.Nifti1Image(vol3d, img.affine, hdr)
                    return vol3d, ref3d
        # BOLD / other 4D: first volume
        vol3d = data[..., 0]
        hdr = img.header.copy()
        hdr.set_data_shape(vol3d.shape)
        ref3d = nib.Nifti1Image(vol3d, img.affine, hdr)
        return vol3d, ref3d

    raise ValueError(f"Unexpected ndim={data.ndim} for {nii_path.name}")


# ─── Intensity normalisation ──────────────────────────────────────────────────


def normalize_volume(arr: np.ndarray, mask: np.ndarray) -> np.ndarray:
    """
    Clip to 1st/99th percentile of brain voxels, rescale to [0, 1].
    For phase-like images: operate on abs(arr).
    """
    tissue = arr[mask > 0]
    if tissue.size < 10:
        return arr.astype(np.float32)
    p1, p99 = np.percentile(tissue, [1, 99])
    clipped = np.clip(arr, p1, p99).astype(np.float32)
    span = p99 - p1
    if span > 0:
        clipped = (clipped - p1) / span
    return clipped


# ─── SimpleITK helpers ────────────────────────────────────────────────────────


def nib_to_sitk(arr: np.ndarray, ref_img: nib.Nifti1Image) -> sitk.Image:
    """Convert a numpy array + nibabel affine → SimpleITK image (with correct mm spacing and orientation)."""
    sitk_img = sitk.GetImageFromArray(arr.T)
    affine = ref_img.affine
    spacing = np.sqrt((affine[:3, :3] ** 2).sum(axis=0))
    # Direction cosines: unit vectors of each voxel axis in physical space
    direction = (affine[:3, :3] / spacing).flatten().tolist()
    sitk_img.SetSpacing(spacing.tolist())
    sitk_img.SetOrigin(affine[:3, 3].tolist())
    sitk_img.SetDirection(direction)
    return sitk_img


# ─── PyRadiomics extractor ────────────────────────────────────────────────────


def build_extractor(config_path: Path) -> featureextractor.RadiomicsFeatureExtractor:
    extractor = featureextractor.RadiomicsFeatureExtractor(str(config_path))
    return extractor


# ─── Per-scan worker ──────────────────────────────────────────────────────────


def extract_one(
    nii_path: Path,
    mask_path: Path | None,
    entities: dict,
    sub: str,
    ses: str,
    modality_id: str,
    acq_tag: str,
    config_path: Path,
    no_mask: bool = False,
) -> dict | None:
    """
    Extract PyRadiomics features from one scan.
    mask_path=None / no_mask=True → whole-volume mask (all ones).
    Returns a flat dict of metadata + features, or None on failure.
    """
    # Silence loggers in subprocess
    for _lg in ("radiomics", "radiomics.glcm", "pykwalify", "pykwalify.core"):
        logging.getLogger(_lg).setLevel(logging.ERROR)

    try:
        vol3d, ref_img = extract_3d(nii_path, entities)

        if no_mask or mask_path is None:
            mask_arr = np.ones(vol3d.shape, dtype=np.float32)
            mask_ref = ref_img
        else:
            mask_img = nib.load(str(mask_path))
            mask_arr = mask_img.get_fdata(dtype=np.float32)
            mask_ref = mask_img

            # Handle shape mismatch
            if vol3d.shape != mask_arr.shape:
                sitk_vol  = nib_to_sitk(vol3d, ref_img)
                sitk_mask = sitk.ReadImage(str(mask_path), sitk.sitkUInt8)
                resampler = sitk.ResampleImageFilter()
                resampler.SetReferenceImage(sitk_vol)
                resampler.SetInterpolator(sitk.sitkNearestNeighbor)
                sitk_mask = resampler.Execute(sitk_mask)
                mask_arr  = sitk.GetArrayFromImage(sitk_mask).T.astype(np.float32)
                mask_ref  = ref_img

        # Normalize intensity inside ROI
        norm_arr = normalize_volume(vol3d, mask_arr)

        # Convert to SimpleITK
        sitk_img    = nib_to_sitk(norm_arr, ref_img)
        sitk_mask_s = nib_to_sitk(mask_arr.astype(np.uint8), mask_ref)
        sitk_mask_s = sitk.Cast(sitk_mask_s, sitk.sitkUInt8)

        # Check ROI has enough voxels
        if int(mask_arr.sum()) < 100:
            return None

        extractor = build_extractor(config_path)
        result    = extractor.execute(sitk_img, sitk_mask_s)

        features: dict[str, float] = {}
        for key, val in result.items():
            if key.startswith("diagnostics_"):
                continue
            # Strip 'original_' prefix added by PyRadiomics
            clean_key = key.replace("original_", "", 1)
            try:
                features[clean_key] = float(val)
            except (TypeError, ValueError):
                pass

        if not features:
            return None

        scanner_model, vendor = parse_scanner(ses)
        meta: dict = {
            "subject":         sub,
            "session":         ses,
            "modality_id":     modality_id,
            "acq_tag":         acq_tag,
            "scanner_model":   scanner_model,
            "scanner_vendor":  vendor,
            "cohort_category": "original",
            "image_path":      str(nii_path),
            "mask_path":       str(mask_path) if mask_path is not None else "no_mask",
        }
        meta.update(features)
        return meta

    except Exception as exc:
        logging.getLogger(__name__).error(
            "FAILED %s: %s", nii_path.name, exc, exc_info=False
        )
        return None


# ─── Main ─────────────────────────────────────────────────────────────────────


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter
    )
    p.add_argument("--bids_dir",        type=Path, default=Path("data/ON-Harmony"))
    p.add_argument("--derivatives_dir", type=Path,
                   default=Path("data/ON-Harmony/derivatives"))
    p.add_argument("--output_csv",      type=Path, default=None,
                   help="Output CSV path. Defaults to outputs/data/original/roi_mask/ or "
                        "outputs/data/original/no_mask/ depending on --no_mask.")
    p.add_argument("--no_mask", action="store_true", default=False,
                   help="Use a whole-volume mask (all ones) instead of the native brain ROI mask.")
    p.add_argument("--config",          type=Path,
                   default=Path("analysis/contrast_manifold/config/radiomics_config.yaml"))
    p.add_argument("--n_workers",  type=int, default=256)
    p.add_argument("--swi_mag_only", action="store_true", default=True,
                   help="SWI: extract only magnitude (part-mag_GRE). Default: True.")
    p.add_argument("--swi_all_parts", dest="swi_mag_only", action="store_false",
                   help="SWI: extract all parts (mag + phase + real + imag)")
    p.add_argument("--swi_echoes", nargs="*", default=None,
                   metavar="N",
                   help="SWI echo numbers to include (default: all). E.g. --swi_echoes 1 4 8")
    p.add_argument("--append", action="store_true",
                   help="Append to existing CSV instead of overwriting")
    p.add_argument("--limit", type=int, default=None,
                   help="Dev mode: process only first N scans")
    return p.parse_args()


def main() -> None:
    args = parse_args()

    if not args.config.exists():
        raise FileNotFoundError(f"PyRadiomics config not found: {args.config}")

    # Resolve output path if not explicitly provided
    if args.output_csv is None:
        subfolder = "no_mask" if args.no_mask else "roi_mask"
        args.output_csv = Path(
            f"analysis/contrast_manifold/outputs/data/original/{subfolder}/on_harmony_features.csv"
        )
    args.output_csv.parent.mkdir(parents=True, exist_ok=True)
    log.info("Output → %s", args.output_csv)

    mode = "whole-volume (no mask)" if args.no_mask else "native brain ROI mask"
    log.info("Discovering scans [mode: %s] …", mode)
    scans = discover_scans(
        bids_root=args.bids_dir,
        deriv_root=args.derivatives_dir,
        swi_mag_only=args.swi_mag_only,
        swi_echoes=[str(e) for e in args.swi_echoes] if args.swi_echoes else None,
        no_mask=args.no_mask,
    )
    log.info("%d scans with masks found", len(scans))

    # Skip already-extracted rows if appending
    done_keys: set[str] = set()
    if args.append and args.output_csv.exists():
        existing = pd.read_csv(args.output_csv, usecols=["image_path"])
        done_keys = set(existing["image_path"].tolist())
        scans = [s for s in scans if str(s["path"]) not in done_keys]
        log.info("%d scans remaining after deduplication", len(scans))

    if args.limit:
        scans = scans[: args.limit]

    log.info("Submitting %d extraction jobs (%d workers) …", len(scans), args.n_workers)

    results: list[dict] = []
    with ProcessPoolExecutor(max_workers=args.n_workers) as pool:
        futures = {
            pool.submit(
                extract_one,
                s["path"], s["mask_path"], s["entities"],
                s["sub"], s["ses"], s["modality_id"], s["acq_tag"],
                args.config, args.no_mask,
            ): s["path"]
            for s in scans
        }
        with tqdm(as_completed(futures), total=len(futures),
                  desc="Extracting features", unit="scan",
                  dynamic_ncols=True) as pbar:
            for fut in pbar:
                src = futures[fut]
                try:
                    rec = fut.result()
                except Exception as exc:
                    pbar.write(f"ERROR {src.name}: {exc}")
                    rec = None

                if rec is not None:
                    results.append(rec)
                elif rec is None and src.name not in ("",):
                    pbar.write(f"WARN  no features: {src.name}")

                pbar.set_postfix(valid=len(results))

    if not results:
        log.error("No features extracted. Check logs.")
        return

    df_new = pd.DataFrame(results)
    META_COLS = [
        "subject", "session", "modality_id", "acq_tag",
        "scanner_model", "scanner_vendor", "cohort_category",
        "image_path", "mask_path",
    ]
    feat_cols = [c for c in df_new.columns if c not in META_COLS]
    df_new = df_new[META_COLS + feat_cols]

    if args.append and args.output_csv.exists():
        df_existing = pd.read_csv(args.output_csv)
        df_out = pd.concat([df_existing, df_new], axis=0, ignore_index=True, sort=False)
    else:
        df_out = df_new

    df_out.to_csv(args.output_csv, index=False)
    log.info(
        "Saved %d rows × %d feature columns → %s",
        len(df_out), len(feat_cols), args.output_csv,
    )


if __name__ == "__main__":
    main()
