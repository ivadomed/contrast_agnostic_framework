MIN_ROI_VOXELS = 50  # skip label if too few voxels after resampling


# ─── Per-scan worker ──────────────────────────────────────────────────────────

def _extract_one_multilabel(
    nii_path: Path,
    label_map_path: Path,
    entities: dict,
    sub: str,
    ses: str,
    modality_id: str,
    acq_tag: str,
    config_path: Path,
    active_labels: dict[int, str],   # {label_id: region_name}
) -> dict | None:
    """
    Extract PyRadiomics features per SynthSeg region for one scan.

    Returns a flat dict:
      metadata keys + {region}_{feature} for every (region, feature) pair.
      Regions with fewer than MIN_ROI_VOXELS after resampling are filled with NaN.
    """
    for _lg in ("radiomics", "radiomics.glcm", "pykwalify", "pykwalify.core"):
        logging.getLogger(_lg).setLevel(logging.ERROR)

    try:
        # ── Load image ────────────────────────────────────────────────────────
        vol3d, ref_img = extract_3d(nii_path, entities)

        # ── Load + resample label map to image space ──────────────────────────
        sitk_vol      = nib_to_sitk(vol3d, ref_img)
        sitk_label    = sitk.ReadImage(str(label_map_path), sitk.sitkUInt8)
        resampler     = sitk.ResampleImageFilter()
        resampler.SetReferenceImage(sitk_vol)
        resampler.SetInterpolator(sitk.sitkNearestNeighbor)
        sitk_label_rs = resampler.Execute(sitk_label)
        label_arr_nat = sitk.GetArrayFromImage(sitk_label_rs).T  # native res

        # ── Normalize intensity using all brain labels as combined mask ────────
        brain_mask = (label_arr_nat > 0).astype(np.float32)
        norm_arr   = normalize_volume(vol3d, brain_mask)
        sitk_img   = nib_to_sitk(norm_arr, ref_img)

        # ── Pre-resample image + labels ONCE to 3 mm (isotropic) ─────────────
        # Avoids BSpline resampling inside PyRadiomics for every one of the 31 labels.
        _TARGET_SP = [3.0, 3.0, 3.0]
        _orig_sp   = list(sitk_img.GetSpacing())
        _orig_sz   = list(sitk_img.GetSize())
        _new_sz    = [max(1, int(round(_orig_sz[i] * _orig_sp[i] / _TARGET_SP[i])))
                      for i in range(3)]

        def _make_rs(interp):
            rs = sitk.ResampleImageFilter()
            rs.SetOutputSpacing(_TARGET_SP)
            rs.SetSize(_new_sz)
            rs.SetOutputOrigin(sitk_img.GetOrigin())
            rs.SetOutputDirection(sitk_img.GetDirection())
            rs.SetTransform(sitk.Transform())
            rs.SetDefaultPixelValue(0)
            rs.SetInterpolator(interp)
            return rs

        sitk_img_3mm   = _make_rs(sitk.sitkBSpline).Execute(sitk_img)
        sitk_label_3mm = _make_rs(sitk.sitkNearestNeighbor).Execute(sitk_label_rs)
        label_arr      = sitk.GetArrayFromImage(sitk_label_3mm).T  # (X,Y,Z)

        extractor = build_extractor(config_path)

        # ── Extract features per label ─────────────────────────────────────────
        all_features: dict[str, float] = {}
        for label_id, region_name in active_labels.items():
            binary_mask = (label_arr == label_id).astype(np.uint8)
            if int(binary_mask.sum()) < MIN_ROI_VOXELS:
                # Fill with NaN — label not present or too small after resampling
                # We'll insert NaN placeholders after we know feature names
                all_features[f"__skip_{region_name}"] = True
                continue

            sitk_mask_r = sitk.GetImageFromArray(binary_mask.T)
            sitk_mask_r.CopyInformation(sitk_img_3mm)
            sitk_mask_r = sitk.Cast(sitk_mask_r, sitk.sitkUInt8)

            try:
                result = extractor.execute(sitk_img_3mm, sitk_mask_r)
            except Exception as exc:
                logging.getLogger(__name__).warning(
                    "PyRadiomics error [%s / %s]: %s", region_name, nii_path.name, exc
                )
                all_features[f"__skip_{region_name}"] = True
                continue

            for key, val in result.items():
                if key.startswith("diagnostics_"):
                    continue
                feat_name = key.replace("original_", "", 1)
                try:
                    all_features[f"{region_name}_{feat_name}"] = float(val)
                except (TypeError, ValueError):
                    pass

        if not all_features or all(k.startswith("__skip_") for k in all_features):
            return None

        # Collect feature names from successful regions
        feat_cols = [k for k in all_features if not k.startswith("__skip_")]
        if not feat_cols:
            return None

        # Infer canonical feature names from first successful region
        first_region = next(v for v in active_labels.values()
                            if f"__skip_{v}" not in all_features)
        feat_suffixes = [
            k[len(first_region) + 1:]
            for k in feat_cols if k.startswith(first_region + "_")
        ]

        # Fill NaN for skipped regions
        for label_id, region_name in active_labels.items():
            if f"__skip_{region_name}" in all_features:
                for suf in feat_suffixes:
                    all_features[f"{region_name}_{suf}"] = float("nan")
                del all_features[f"__skip_{region_name}"]

        scanner_model, vendor = parse_scanner(ses)
        meta: dict = {
            "subject":         sub,
            "session":         ses,
            "modality_id":     modality_id,
            "acq_tag":         acq_tag,
            "scanner_model":   scanner_model,
            "scanner_vendor":  vendor,
            "cohort_category": "original",
            "image_path":      str(nii_path),
            "label_map_path":  str(label_map_path),
        }
        meta.update(all_features)
        return meta

    except Exception as exc:
        logging.getLogger(__name__).error(
            "FAILED %s: %s", nii_path.name, exc, exc_info=False
        )
        return None


# ─── Main ─────────────────────────────────────────────────────────────────────

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--bids-root", type=Path,
                   default=PROJECT_ROOT / "data" / "ON-Harmony")
    p.add_argument("--derivatives-root", type=Path,
                   default=PROJECT_ROOT / "data" / "ON-Harmony" / "derivatives")
    p.add_argument("--synthseg-root", type=Path, default=None,
                   help="Root of SynthSeg masks "
                        "(default: <derivatives-root>/synthseg_masks)")
    p.add_argument("--output-csv", type=Path, default=None,
                   help="Output CSV. Defaults to "
                        "outputs/data/original/synthseg_mask_{N}/on_harmony_features.csv")
    p.add_argument("--config", type=Path,
                   default=PROJECT_ROOT / "analysis" / "contrast_manifold"
                           / "config" / "radiomics_config.yaml")
    p.add_argument("--label-subset", type=str, default=None,
                   help="Comma-separated label IDs to use. Default: all 31 labels. "
                        "Example: --label-subset 2,3,41,42")
    p.add_argument("--n_workers",  type=int, default=4)
    p.add_argument("--append",     action="store_true")
    p.add_argument("--limit",      type=int, default=None)
    p.add_argument("--rank",       type=int, default=0)
    p.add_argument("--world-size", type=int, default=1)
    return p.parse_args()


def main() -> None:
    args = parse_args()

    if args.synthseg_root is None:
        args.synthseg_root = args.derivatives_root / "synthseg_masks"

    # Resolve active labels
    if args.label_subset:
        ids = [int(x.strip()) for x in args.label_subset.split(",")]
        active_labels = {i: ALL_LABELS[i] for i in ids if i in ALL_LABELS}
        if not active_labels:
            log.error("No valid label IDs in --label-subset %s", args.label_subset)
            sys.exit(1)
    else:
        active_labels = ALL_LABELS

    n_labels = len(active_labels)

    if args.output_csv is None:
        args.output_csv = (
            PROJECT_ROOT / "analysis" / "contrast_manifold" / "outputs"
            / "data" / "original" / f"synthseg_mask_{n_labels}"
            / "on_harmony_features.csv"
        )
    args.output_csv.parent.mkdir(parents=True, exist_ok=True)
    log.info("Output → %s  (%d labels)", args.output_csv, n_labels)

    if not args.config.exists():
        raise FileNotFoundError(f"PyRadiomics config not found: {args.config}")

    log.info("Discovering scans …")
    scans = discover_scans(
        bids_root=args.bids_root,
        deriv_root=args.derivatives_root,
        swi_mag_only=True,
        no_mask=True,  # we use SynthSeg labels instead of brain mask
    )

    # Keep only scans that have a T1w SynthSeg label map for their session
    def _label_map(s: dict) -> Path | None:
        sub, ses = s["sub"], s["ses"]
        # Pad sub/ses if they arrived without prefix
        sub_dir = f"sub-{sub}" if not sub.startswith("sub-") else sub
        ses_dir = f"ses-{ses}" if not ses.startswith("ses-") else ses
        # T1w synthseg filename: sub-X_ses-Y_T1w_synthseg.nii.gz
        p = (args.synthseg_root / sub_dir / ses_dir / "anat"
             / f"{sub_dir}_{ses_dir}_T1w_synthseg.nii.gz")
        return p if p.exists() else None

    scans_with_map = []
    for s in scans:
        lm = _label_map(s)
        if lm is not None:
            s["label_map"] = lm
            scans_with_map.append(s)
    log.info("%d/%d scans have a T1w SynthSeg label map", len(scans_with_map), len(scans))

    if args.world_size > 1:
        scans_with_map = scans_with_map[args.rank :: args.world_size]
        log.info("Rank %d/%d → %d scans", args.rank, args.world_size, len(scans_with_map))

    if args.limit:
        scans_with_map = scans_with_map[: args.limit]

    # Deduplication when appending
    done_keys: set[str] = set()
    if args.append and args.output_csv.exists():
        existing = pd.read_csv(args.output_csv, usecols=["image_path"])
        done_keys = set(existing["image_path"].tolist())
        scans_with_map = [s for s in scans_with_map
                          if str(s["path"]) not in done_keys]
        log.info("%d scans remaining after dedup", len(scans_with_map))

    log.info("Submitting %d jobs (%d workers) …", len(scans_with_map), args.n_workers)

    results: list[dict] = []
    with ProcessPoolExecutor(max_workers=args.n_workers) as pool:
        futures = {
            pool.submit(
                _extract_one_multilabel,
                s["path"], s["label_map"], s["entities"],
                s["sub"], s["ses"], s["modality_id"], s["acq_tag"],
                args.config, active_labels,
            ): s["path"]
            for s in scans_with_map
        }
        with tqdm(as_completed(futures), total=len(futures),
                  desc="Extracting features", unit="scan",
                  dynamic_ncols=True) as pbar:
            for fut in pbar:
                src = futures[fut]
                try:
                    rec = fut.result()
                except Exception as exc:
                    pbar.write(f"ERROR {src.name}: {exc}")
                    rec = None
                if rec is not None:
                    results.append(rec)
                else:
                    pbar.write(f"WARN  no features: {src.name}")
                pbar.set_postfix(valid=len(results))

    if not results:
        log.error("No features extracted.")
        return

    META_COLS = [
        "subject", "session", "modality_id", "acq_tag",
        "scanner_model", "scanner_vendor", "cohort_category",
        "image_path", "label_map_path",
    ]
    df_new    = pd.DataFrame(results)
    feat_cols = [c for c in df_new.columns if c not in META_COLS]
    df_new    = df_new[META_COLS + feat_cols]

    # Each rank writes to its own shard to avoid concurrent-write races.
    if args.world_size > 1:
        shard_csv = args.output_csv.with_name(
            args.output_csv.stem + f"_rank{args.rank}" + args.output_csv.suffix
        )
    else:
        shard_csv = args.output_csv

    if args.append and shard_csv.exists():
        df_existing = pd.read_csv(shard_csv)
        df_out = pd.concat([df_existing, df_new], axis=0, ignore_index=True, sort=False)
    else:
        df_out = df_new

    df_out.to_csv(shard_csv, index=False)
    log.info("Saved %d rows × %d columns → %s",
             len(df_out), len(df_out.columns), shard_csv)

    # Merge all shards into final CSV when all ranks are done
    if args.world_size > 1:
        shards = sorted(args.output_csv.parent.glob(
            args.output_csv.stem + "_rank*" + args.output_csv.suffix
        ))
        if len(shards) == args.world_size:
            log.info("All %d shards present — merging …", args.world_size)
            df_merged = pd.concat([pd.read_csv(s) for s in shards],
                                  axis=0, ignore_index=True, sort=False)
            df_merged.to_csv(args.output_csv, index=False)
            log.info("Merged → %d rows × %d columns → %s",
                     len(df_merged), len(df_merged.columns), args.output_csv)
        else:
            log.info("Shard %d/%d done (waiting for others before merge)",
                     args.rank + 1, args.world_size)


if __name__ == "__main__":
    main()
