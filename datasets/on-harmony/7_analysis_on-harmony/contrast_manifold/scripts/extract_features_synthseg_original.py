#!/usr/bin/env python
"""
Extract per-region PyRadiomics features for all original ON-Harmony scans
using SynthSeg label maps (generated by run_synthseg.py).

For each scan, the T1w SynthSeg label map for that subject/session is
resampled to the scan's voxel space, then PyRadiomics is run independently
for each SynthSeg region.  Results are stored in wide format:
  {region_name}_{feature_name}  e.g.  left_cerebral_cortex_glcm_Contrast

Output CSV goes under:
  analysis/contrast_manifold/outputs/data/original/synthseg_mask_{N}/

where N is the number of regions extracted (all 31 by default).

Usage (single slot):
  set_slot 0 .venv/bin/python \\
    analysis/contrast_manifold/scripts/extract_features_synthseg_original.py \\
    --n_workers 8

Usage (4 parallel slots):
  for rank in 0 1 2 3; do
    set_slot $rank .venv/bin/python \\
      analysis/contrast_manifold/scripts/extract_features_synthseg_original.py \\
      --n_workers 4 --rank $rank --world-size 4 \\
      > /tmp/feat_synth_orig_rank${rank}.log 2>&1 < /dev/null &
  done
  wait && echo "All done."

Dev / dry-run (1 subject):
  set_slot 0 .venv/bin/python \\
    analysis/contrast_manifold/scripts/extract_features_synthseg_original.py \\
    --limit 1

To restrict to a subset of label IDs (e.g. cortex + WM only):
  --label-subset 2,3,41,42
"""
from __future__ import annotations

